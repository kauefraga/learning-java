public class Variaveis {
    public static void main(String[] args) {
        int x; // Declaração
        x = 123; // Atribuição
        int y = 321; // Inicialização

        System.out.println("x: " + x);
        System.out.println("y: " + y);

        double pi = 3.14;
        boolean isYourMotherSingle = false;
        String yourMothersName = "Jossílainëh";

        System.out.println("PI: " + pi);
        System.out.println("Tua mãe tá solteira? " + isYourMotherSingle);
        System.out.println("Nome da tua mãe: " + yourMothersName);
    }
}
