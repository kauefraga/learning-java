public class WrapperClasses {
    public static void main(String[] args) {
        /*
            PRIMITIVOS  |  REFERENCE DATA TYPES
            boolean     |  Boolean
            char        |  Character
            int         |  Integer
            double      |  Double

            autoboxing -> conversão automática (feita pelo compilador)
            de correspondência entre um tipo primitivo e sua wrapper class
            unboxing -> o contrário de autoboxing. Conversão automática de
            wrapper class para primitivo
         */

        Boolean isSomething = true;
        Character someChar = '@';
        Integer someNumber = 1241;
        Double someDecimalNumber = 12.312;
        String e = "Kauê";

        // Reference data types podem ter métodos muito úteis
        // Mas, com certeza, tipos primitivos são mais rápidos
    }
}
